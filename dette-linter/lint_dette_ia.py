def scan_repo(repo: Path, deep: bool = False) -> dict:
    """Scanne un repo et renvoie le rapport JSON-able (un objet)."""
    repo = repo.resolve()
    report: dict = {
        'repo': repo.name,
        'etudiant': repo.name,
        'total': 0,
        'score': 0,
        'lignesDeCode': 0,
        'modeDeep': deep,
        'parRegle': {key: 0 for key in RULES_LABELS},
        'parSeverite': {sev: 0 for sev in SEVERITY_WEIGHT},
        'exemples': [],
    }

    files = list(iter_source_files(repo))

    # Repo vide ou illisible : marque non analysable, ne crashe pas.
    if not files:
        report['nonAnalysable'] = True
        report['raison'] = 'aucun fichier source detecte'
        return report

    examples_par_regle: dict[str, list[dict]] = {key: [] for key in RULES_LABELS}

    def add_finding(rule: str, rel_file: str, line: int, detail: str,
                    contexte: list[dict] | None = None) -> None:
        report['parRegle'][rule] += 1
        report['total'] += 1
        bucket = examples_par_regle[rule]
        if len(bucket) < MAX_EXAMPLES_PER_RULE:
            exemple = {
                'regle': rule,
                'fichier': rel_file,
                'ligne': line,
                'detail': detail,
            }
            if contexte:
                exemple['contexte'] = contexte
            bucket.append(exemple)

    # --- Regle repo-level : pas de cadre (#1/#5) ---
    has_framing = any(
        entry.is_file() and entry.name.lower() in FRAMING_FILES
        for entry in repo.iterdir()
    )
    if not has_framing:
        add_finding('pas-de-cadre', '.', 1,
                    'aucun CLAUDE.md / PROJECT_RULES.md a la racine')

    # --- Regle repo-level : aucun test ---
    has_tests = any(TEST_FILE.search(str(p.relative_to(repo))) for p in files)
    if not has_tests:
        add_finding('aucun-test', '.', 1,
                    'aucun fichier de test (.test/.spec/__tests__/cypress)')

    # --- Scan fichier par fichier ---
    for path in files:
        rel = str(path.relative_to(repo))
        content = is_text_readable(path)
        if content is None:
            continue
        # Code genere (Prisma, codegen...) : pas la dette de l'etudiant, on saute
        if is_generated(content):
            continue
        lines = content.split('\n')

        # Lignes de code de l'etudiant (fichiers source non generes).
        # splitlines() = compte correct (pas de fausse ligne vide finale, colle a
        # wc -l / aux editeurs). `lines` (split) reste pour l'indexation snippet.
        nb_lines = len(content.splitlines())
        report['lignesDeCode'] += nb_lines

        # God file (#10)
        if nb_lines > GOD_FILE_WARNING:
            gravite = 'grave' if nb_lines > GOD_FILE_GRAVE else 'warning'
            add_finding('god-file', rel, 1, f'{nb_lines} lignes ({gravite})')

        # Nommage paresseux (#4) : stem du fichier
        if path.stem.lower() in LAZY_FILE_STEMS:
            add_finding('nommage-paresseux', rel, 1,
                        f'fichier fourre-tout : {path.name}')

        # Boilerplate non modifie : 1 souci par fichier (premiere ligne marqueur)
        bp = BOILERPLATE_MARKERS.search(content)
        if bp:
            line_no = line_of(content, bp.start())
            add_finding('boilerplate-non-modifie', rel, line_no,
                        'page de scaffold (create-next-app/Vite) non modifiee',
                        build_context(lines, line_no))

        # Donnees mockees : import d'un module mock/fake comme source de donnees
        seen_lines = set()
        for match in MOCK_IMPORT.finditer(content):
            line_no = line_of(content, match.start())
            if line_no in seen_lines:
                continue
            seen_lines.add(line_no)
            add_finding('donnees-mockees', rel, line_no,
                        'import de donnees mockees (pas de vrai backend)',
                        build_context(lines, line_no))

        # Fichiers de test/fixture : secrets et logs y sont attendus (fixtures)
        is_test_fixture = bool(SKIP_LOGS_FILE_HINT.search(rel))

        is_python = path.suffix.lower() == '.py'

        # Regles "ligne" a regex (dedupe par ligne dans une meme regle)
        for rule, patterns in LINE_RULES.items():
            if is_test_fixture and rule in TEST_SKIP_RULES:
                continue
            if is_python and rule in JS_ONLY_RULES:
                continue
            seen_lines: set[int] = set()
            for pattern in patterns:
                for match in pattern.finditer(content):
                    line_no = line_of(content, match.start())
                    if line_no in seen_lines:
                        continue
                    seen_lines.add(line_no)
                    add_finding(rule, rel, line_no, snippet_at(lines, line_no),
                                build_context(lines, line_no))

        # Logs oublies (#10/#11) : selon langage, hors fichiers test/seed/mock
        if not is_test_fixture:
            log_patterns = (LOGS_PY_PATTERNS if path.suffix.lower() == '.py'
                            else LOGS_JS_PATTERNS)
            seen_lines = set()
            for pattern in log_patterns:
                for match in pattern.finditer(content):
                    line_no = line_of(content, match.start())
                    if line_no in seen_lines:
                        continue
                    seen_lines.add(line_no)
                    add_finding('logs-oublies', rel, line_no,
                                snippet_at(lines, line_no),
                                build_context(lines, line_no))

        # Type any : echappatoire TypeScript (uniquement fichiers .ts/.tsx)
        if path.suffix.lower() in TS_EXTENSIONS:
            seen_lines = set()
            for pattern in TYPE_ANY:
                for match in pattern.finditer(content):
                    line_no = line_of(content, match.start())
                    if line_no in seen_lines:
                        continue
                    seen_lines.add(line_no)
                    add_finding('type-any', rel, line_no,
                                snippet_at(lines, line_no),
                                build_context(lines, line_no))

        # Code commente (mort) : commentaire qui ressemble a du code, hors test
        if not is_test_fixture:
            seen_lines = set()
            for match in COMMENTED_CODE.finditer(content):
                line_no = line_of(content, match.start())
                if line_no in seen_lines:
                    continue
                seen_lines.add(line_no)
                add_finding('code-commente', rel, line_no,
                            snippet_at(lines, line_no),
                            build_context(lines, line_no))

        # Hooks React sans tableau de dependances (comptage de parentheses)
        if not is_python:
            for offset in react_hooks_missing_deps(content):
                line_no = line_of(content, offset)
                add_finding('react-hook-sans-deps', rel, line_no,
                            snippet_at(lines, line_no),
                            build_context(lines, line_no))

    # Mode deep : outils externes (gitleaks/trivy/jscpd/tsc) + AST (tree-sitter).
    # Import tolerant : si le module deep n'est pas la (distribution standard),
    # on ignore proprement au lieu de planter.
    if deep:
        try:
            from lint_dette_deep import run_deep_checks
        except ImportError:
            run_deep_checks = None
        if run_deep_checks is None:
            report['deepStatus'] = {'erreur': 'module lint_dette_deep.py absent'}
        else:
            result = run_deep_checks(repo, files, is_generated)
            report['deepStatus'] = result['status']
            for rule, rel_file, line, detail in result['findings']:
                add_finding(rule, rel_file, line, detail)

    # Aplati les exemples (variete : quelques-uns par regle)
    for bucket in examples_par_regle.values():
        report['exemples'].extend(bucket)

    # Score pondere par gravite + repartition critique/majeur/mineur
    for rule, count in report['parRegle'].items():
        severite = RULE_SEVERITY[rule]
        report['score'] += count * SEVERITY_WEIGHT[severite]
        report['parSeverite'][severite] += count

    return report


def scan_all(folder: Path, deep: bool = False) -> list[dict]:
    """Scanne chaque sous-dossier (= un repo clone) et renvoie un tableau."""
    folder = folder.resolve()
    reports: list[dict] = []
    for entry in sorted(folder.iterdir()):
        if not entry.is_dir():
            continue
        if entry.name in IGNORE_DIRS:
            continue
        try:
            reports.append(scan_repo(entry, deep=deep))
        except Exception as exc:  # robustesse : un repo casse n'arrete pas tout
            reports.append({
                'repo': entry.name,
                'etudiant': entry.name,
                'total': 0,
                'score': 0,
                'lignesDeCode': 0,
                'modeDeep': deep,
                'parRegle': {key: 0 for key in RULES_LABELS},
                'parSeverite': {sev: 0 for sev in SEVERITY_WEIGHT},
                'exemples': [],
                'nonAnalysable': True,
                'raison': f'erreur de scan : {exc}',
            })
    return reports


# --------------------------------------------------------------------------
# Historique : empile un instantane par analyse, pour suivre l'evolution
# --------------------------------------------------------------------------

def append_history(path: Path, reports: list[dict]) -> None:
    """Ajoute un instantane horodate (par repo analyse) au fichier historique."""
    try:
        history = json.loads(path.read_text(encoding='utf-8')) if path.exists() else []
    except (json.JSONDecodeError, OSError):
        history = []
    if not isinstance(history, list):
        history = []
    when = datetime.now().isoformat(timespec='seconds')
    for r in reports:
        if r.get('nonAnalysable'):
            continue
        history.append({
            'date': when,
            'repo': r.get('repo'),
            'etudiant': r.get('etudiant'),
            'score': r.get('score', 0),
            'total': r.get('total', 0),
            'lignesDeCode': r.get('lignesDeCode', 0),
            'parSeverite': r.get('parSeverite', {}),
        })
    path.write_text(json.dumps(history, ensure_ascii=False, indent=2), encoding='utf-8')


