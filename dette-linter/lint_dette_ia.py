def main() -> int:
    parser = argparse.ArgumentParser(
        description="Linter de dette IA (deterministe, zero LLM).",
    )
    parser.add_argument('chemin', help="chemin d'un repo, ou dossier de repos avec --all")
    parser.add_argument('--all', action='store_true',
                        help='scanne tous les sous-dossiers comme autant de repos')
    parser.add_argument('--pretty', action='store_true',
                        help='JSON indente et lisible')
    parser.add_argument('--deep', action='store_true',
                        help='lance aussi les outils externes (gitleaks, trivy, '
                             'jscpd, tsc) + AST tree-sitter. Plus lent, toujours '
                             'deterministe, zero LLM.')
    parser.add_argument('--history', metavar='FICHIER',
                        help='empile un instantane horodate de cette analyse dans '
                             'FICHIER (JSON) pour suivre l evolution dans le temps')
    args = parser.parse_args()

    cible = Path(args.chemin)
    if not cible.exists():
        print(f"Chemin introuvable : {cible}", file=sys.stderr)
        return 1

    result = (scan_all(cible, deep=args.deep) if args.all
              else scan_repo(cible, deep=args.deep))

    if args.history:
        reports = result if isinstance(result, list) else [result]
        append_history(Path(args.history), reports)

    indent = 2 if args.pretty else None
    print(json.dumps(result, ensure_ascii=False, indent=indent))
    return 0


if __name__ == '__main__':
    raise SystemExit(main())
