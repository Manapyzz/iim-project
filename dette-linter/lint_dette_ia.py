def line_of(content: str, offset: int) -> int:
    """Numero de ligne (1-indexe) correspondant a un offset dans le contenu."""
    return content.count('\n', 0, offset) + 1


def snippet_at(lines: list[str], line_no: int, limit: int = 120) -> str:
    """Renvoie la ligne ciblee, nettoyee et tronquee, pour l'affichage."""
    if 1 <= line_no <= len(lines):
        text = lines[line_no - 1].strip()
        return text[:limit]
    return ''


def react_hooks_missing_deps(content: str) -> list[int]:
    """Offsets des hooks React appeles sans tableau de dependances.

    Match les parentheses de l'appel : un hook avec deps finit par `])`, un hook
    sans deps finit par `})` (le callback est le seul argument) => bug classique
    (effet rejoue a chaque rendu). Deterministe (comptage de parentheses).
    """
    offsets: list[int] = []
    for match in REACT_HOOKS.finditer(content):
        open_paren = match.end() - 1
        depth = 0
        i = open_paren
        while i < len(content):
            if content[i] == '(':
                depth += 1
            elif content[i] == ')':
                depth -= 1
                if depth == 0:
                    break
            i += 1
        call = content[open_paren:i + 1].rstrip()
        # Se termine par "])" => tableau de deps present. Sinon : manquant.
        if not re.search(r'\]\s*\)$', call):
            offsets.append(match.start())
    return offsets


def is_generated(content: str) -> bool:
    """Detecte un fichier genere via son banniere (15 premieres lignes)."""
    head = '\n'.join(content.split('\n')[:15])
    return bool(GENERATED_MARKERS.search(head))


def build_context(lines: list[str], line_no: int) -> list[dict]:
    """Fenetre de code autour de la ligne fautive (vue GitHub).

    Renvoie une liste de {n, code, cible} : numero de ligne, code brut tronque,
    et un flag indiquant la ligne en cause.
    """
    start = max(1, line_no - CONTEXT_RADIUS)
    end = min(len(lines), line_no + CONTEXT_RADIUS)
    return [
        {
            'n': n,
            'code': lines[n - 1][:MAX_CODE_LINE_LENGTH],
            'cible': n == line_no,
        }
        for n in range(start, end + 1)
    ]


def is_text_readable(path: Path) -> str | None:
    """Lit le fichier en texte ; renvoie None si binaire / minifie / illisible."""
    try:
        content = path.read_text(encoding='utf-8', errors='strict')
    except (UnicodeDecodeError, OSError):
        return None
    # Heuristique minifie : une ligne tres longue => on saute
    for chunk in content.split('\n'):
        if len(chunk) > MAX_LINE_LENGTH_BEFORE_MINIFIED:
            return None
    return content


def iter_source_files(repo: Path):
    """Itere les fichiers source pertinents, en sautant les dossiers ignores."""
    for path in sorted(repo.rglob('*')):
        if not path.is_file():
            continue
        parts = set(path.relative_to(repo).parts)
        if parts & IGNORE_DIRS:
            continue
        if path.name in IGNORE_FILES:
            continue
        if '.min.' in path.name:
            continue
        if path.suffix.lower() in SCAN_EXTENSIONS:
            yield path


